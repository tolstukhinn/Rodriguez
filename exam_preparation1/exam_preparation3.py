# Задача 4 (Функции: возвращение данных).
# Создайте функцию find_max, которая принимает список чисел и возвращает максимальное число из этого списка.
# Не используйте встроенную функцию max() — реализуйте логику самостоятельно (через цикл).
# Вызовите функцию со списком [4, 9, 2, 15, 3] и выведите результат.

my_list = [4, 9, 2, 15, 3]

def find_max(numbers):
    maximum = numbers[0]
    for number in numbers:
        if number > maximum:
            maximum = number
    return maximum

print(find_max(my_list))

