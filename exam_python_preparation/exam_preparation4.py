# Задача 10 (Наследование: вызов родительского метода).
# Создайте родительский класс Animal с метод speak, который возвращает "Звук животного".
# Создайте дочерний класс Dog, который наследует от Animal.
# В классе Dog переопределите метод speak, чтобы он:
# Сначала вызывал родительский метод speak (используйте super())
# Затем возвращал строку "Гав-гав!"
# Создайте объект dog = Dog() и вызовите dog.speak(). Выведите результат.
# Подсказка: нужно объединить результаты двух методов.

class Animal:
    def speak(self):
        return "Звук животного"
class Dog(Animal):
    def speak(self):
        gav = super().speak()
        return f"{gav} Гав-гав!"
dog = Dog()
print(dog.speak())






















































