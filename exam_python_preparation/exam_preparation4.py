# Задача 5 (ООП: класс с вычисляемым свойством).
# Создайте класс Circle (Круг) с конструктором __init__,
# принимающим параметр radius (радиус, число). Сохраните его в self.radius.
# Добавьте метод area, который возвращает площадь круга по формуле:
# π * radius² (используйте math.pi или приближение 3.14159).
# Создайте объект circle = Circle(5), вызовите метод area и выведите результат.
# Примечание: импортируйте math или используйте приближённое значение.

class Circle:
    def __init__(self, radius):
        self.radius = radius
    def area(self):
        return math.pi * self.radius ** 2
circle = Circle(5)
print(circle.area())

