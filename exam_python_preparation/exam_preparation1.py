# Задача 10 (Наследование).
# Создайте родительский класс Vehicle с конструктором __init__,
# принимающим параметр max_speed (максимальная скорость, целое число). Сохраните его в self.max_speed.
# Добавьте метод info, который возвращает строку: "Максимальная скорость: [max_speed] км/ч".
# Создайте дочерний класс Car, который наследует от Vehicle.
# В классе Car:
# Добавьте конструктор __init__, принимающий два параметра: max_speed и brand (марка автомобиля).
# Используйте super().__init__(max_speed) для инициализации родительского атрибута.
# Сохраните brand в self.brand.
# Переопределите метод info, чтобы он возвращал строку:
# "Марка: [brand], Максимальная скорость: [max_speed] км/ч".
# Создайте объект класса Car с максимальной скоростью 200 и маркой "Audi".
# Вызовите метод info и выведите результат.

class Vehicle:
    def __init__(self, max_speed):
        self.max_speed = max_speed
    def info(self):
        return f"Максимальная скорость: {self.max_speed} км/ч"

class Car(Vehicle):
    def __init__(self, max_speed, brand):
        super().__init__(max_speed)
        self.brand = brand
    def info(self):
        return f"Марка: {self.brand}, Максимальная скорость: {self.max_speed} км/ч"

my_car = Car(200, "Audi")
print(my_car.info())

