# Задача 4.
# Создайте список: colors = ["red", "blue", "green", "yellow", "stop", "black"]
# Напишите код, который с помощью цикла for перебирает элементы списка и выводит их,
# но прерывает выполнение цикла если встречает элемент "stop".
# Вывод должен содержать только элементы до "stop" (не включая его).

colors = ["red", "blue", "green", "yellow", "stop", "black"]
for color in colors:
    if color == "stop":
        break
    print(color)

# Задача 5.
# Создайте список: numbers = [1, 2, 3, 4, 5]
# Напишите код, который с помощью цикла for и функции range() выводит каждый элемент списка numbers по порядку.
# Каждый элемент должен выводиться на отдельной строке.
# Подсказка: используйте длину списка в функции range().

numbers = [1, 2, 3, 4, 5]
for number in range(len(numbers)):
    print(numbers[number])

