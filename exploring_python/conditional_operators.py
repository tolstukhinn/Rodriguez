# Задача 7.
# Напишите код, который проверяет значение переменной score = 85 и выводит оценку:
# "Отлично", если score ≥ 90
# "Хорошо", если score ≥ 70 и < 90
# "Удовлетворительно", если score ≥ 50 и < 70
# "Неудовлетворительно", если score < 50
# Используйте операторы if, elif, else и логические операции.

score = 85
if score >= 90:
    print("Отлично")
elif score >= 70 and score < 90:
    print("Хорошо")
elif score >= 50 and score < 70:
    print("Удовлетворительно")
else:
    print("Неудовлетворительно")

