# Задача 5.
# Напишите код, который проверяет значение переменной number = 7 и выводит:
# "Число положительное и четное", если число больше 0 И четное
# "Число не соответствует условиям" во всех остальных случаях.
# Используйте логическую операцию and для проверки двух условий в одном if.

number = 7
if number > 0 and number % 2 == 0:
    print("Число положительное и четное")
else:
    print("Число не соответствует условиям")

# Та же логика с тернарным оператором

number = 10
result = "Число положительное и четное" if number > 0 and number % 2 == 0 else "Число не соответствует условиям"
print(result)

# Задача 6.
