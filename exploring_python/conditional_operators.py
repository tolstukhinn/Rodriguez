
# Задача 1.
#Напишите код, который проверяет значение переменной temperature = 25 и выводит:
#"Жарко", если температура больше 30
#"Прохладно", если температура меньше или равна 15
#"Тепло" во всех остальных случаях.
#Используйте только операторы if, elif, else.

temperature = 25
if temperature > 30:
    print("Жарко")
elif temperature <= 15:
    print("Прохладно")
else:
    print("Тепло")

